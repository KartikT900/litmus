function sum(num1, num2) {
  return num1 + num2;
}

function sum1(num3, num4) {
  return num3 + num4;
}

function sum2(num5, num6) {
  return num5 + num6;
}

function sum3(num7, num8) {
  return num7 + num8;
}

function sum4(num9, num10, num11) {
  return num9 + num10 + num11;
}

module.exports = {
  sum,
  sum1,
  sum2,
  sum3,
  sum4
};
